# Declaramos la lista en blanco
numeros = []

# Declaramos el limite en 1
limit = 1

# Mientras el limite no sea mayor que 100, se ejecutara el while
while limit <= 100:
    # Agregando el numero a la lista declarada inicialmente
    numeros.append(limit)
    # Aumentando en 1 la variable limit
    limit = limit + 1

# Imprimiendo los resultados
print(numeros)